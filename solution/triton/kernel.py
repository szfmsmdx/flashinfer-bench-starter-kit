def expand_gqa_heads(x: torch.Tensor) -> torch.Tensor:
    return x.float().repeat_interleave(GDN_GROUP_SIZE, dim=-2)


def normalize_scale(scale: float | torch.Tensor | None) -> float:
    if scale is None:
        return 1.0 / math.sqrt(GDN_HEAD_SIZE)
    if isinstance(scale, torch.Tensor):
        if scale.numel() == 0:
            return 1.0 / math.sqrt(GDN_HEAD_SIZE)
        scale_value = float(scale.item())
    else:
        scale_value = float(scale)
    if scale_value == 0.0:
        return 1.0 / math.sqrt(GDN_HEAD_SIZE)
    return scale_value


def compute_gate_and_beta(
    A_log: torch.Tensor,
    a: torch.Tensor,
    dt_bias: torch.Tensor,
    b: torch.Tensor,
) -> tuple[torch.Tensor, torch.Tensor]:
    x = a.float() + dt_bias.float()
    g = torch.exp(-torch.exp(A_log.float()) * F.softplus(x))
    beta = torch.sigmoid(b.float())
    return g, beta


def init_state_hkv(state: torch.Tensor | None, batch_size: int, device: torch.device) -> torch.Tensor:
    if state is None:
        return torch.zeros(
            batch_size,
            GDN_NUM_V_HEADS,
            GDN_HEAD_SIZE,
            GDN_HEAD_SIZE,
            device=device,
            dtype=torch.float32,
        )
    return state.float().transpose(-1, -2).contiguous()


def _can_use_triton(*tensors: torch.Tensor) -> bool:
    if _DISABLE_TRITON or not _TRITON_AVAILABLE:
        return False
    if not tensors:
        return False
    device = tensors[0].device
    if device.type != "cuda":
        return False
    return all(t.is_cuda and t.device == device for t in tensors)


def _gdn_step_torch(
    q_t: torch.Tensor,
    k_t: torch.Tensor,
    v_t: torch.Tensor,
    state_hkv: torch.Tensor,
    g_t: torch.Tensor,
    beta_t: torch.Tensor,
    scale: float,
) -> tuple[torch.Tensor, torch.Tensor]:
    g_scale = g_t.unsqueeze(-1).unsqueeze(-1)
    beta_scale = beta_t.unsqueeze(-1)

    old_state = state_hkv * g_scale
    flat_state = old_state.reshape(-1, GDN_HEAD_SIZE, GDN_HEAD_SIZE)
    flat_k = k_t.reshape(-1, GDN_HEAD_SIZE)
    flat_q = q_t.reshape(-1, GDN_HEAD_SIZE)

    old_v = torch.bmm(flat_k.unsqueeze(1), flat_state).squeeze(1).reshape_as(v_t)
    delta_v = (v_t - old_v) * beta_scale
    new_state = torch.baddbmm(
        flat_state,
        flat_k.unsqueeze(-1),
        delta_v.reshape(-1, 1, GDN_HEAD_SIZE),
    ).reshape_as(old_state)
    output = scale * torch.bmm(flat_q.unsqueeze(1), new_state.reshape(-1, GDN_HEAD_SIZE, GDN_HEAD_SIZE))
    return output.squeeze(1).reshape_as(v_t), new_state


def _gdn_step_decode_torch(
    q_t: torch.Tensor,
    k_t: torch.Tensor,
    v_t: torch.Tensor,
    state_hkv: torch.Tensor,
    g_t: torch.Tensor,
    beta_t: torch.Tensor,
    scale: float,
) -> tuple[torch.Tensor, torch.Tensor]:
    g_scale = g_t.unsqueeze(-1).unsqueeze(-1)
    beta_scale = beta_t.unsqueeze(-1)

    old_state = state_hkv * g_scale
    flat_state = old_state.reshape(-1, GDN_HEAD_SIZE, GDN_HEAD_SIZE)
    flat_k = k_t.reshape(-1, GDN_HEAD_SIZE)
    flat_q = q_t.reshape(-1, GDN_HEAD_SIZE)

    # Decode only has one recurrent step, so it's worth specializing output
    # as q @ old_state + (q · k) * delta_v to avoid a second full state matvec.
